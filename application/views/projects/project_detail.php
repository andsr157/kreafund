            Risks and challenges
          </h2>
          <p class="main-text">

            Quite unsurprisingly, the most foreseeable issue we may struggle with is the release date.
            While we optimistically set it to December 2024, we know from experience that a lot can go
            wrong overnight when you develop a video game. This media involves so many fields that it's
            hard to tell which one will cause trouble next!

            Additionally, we did not have a chance to delve too profoundly into the different console
            ports yet. Of course we shall do our best to release the game on as many systems and platforms
            as possible, but be wary that Liam_99 may not make it onto your favorite console!
            Learn about accountability on Kickstarter
            Environmental commitments
            Visit our Environmental Resources Center to learn how Kickstarter encourages sustainable practices.
            Long-lasting design

            All-in-one goal of the crowdfunding and core reward for the backers, the game should be playable for years. It's especially true as we propose the game DRM-free, which means PC players don't have to launch the game through platforms such as Steam or Epic Games.

            Regarding physical rewards:

            - We'll work with a local print shop to bring the paper posters to life. This shall allow us to check quality and minimize environmental costs linked to transport.

            - The T-shirts will be produced by a French company. We know from our own experience that they work with lasting materials.

            - The metallic posters will be handled by Displate. It's super sturdy, looks super cool, and the society works with communities in Africa to plant trees and improve the locals' quality of life.
