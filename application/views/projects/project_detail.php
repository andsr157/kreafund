            In November 2015, a national election was held and the National League
            for Democracy (NLD) won in a landslide.
            Later the following year, the winning party had a chance to appoint the
            president and key members of the
            government. I wasn't sure if it was real, so I traveled to Burma in
            February of 2016 to see for myself,
            with my feet on the ground, whether or not this election was for real.
            It seemed real. . .
            Rangoon fireworks
            Rangoon fireworks

            Fast forward five tumultuous years when another national election was
            held, and the NLD again,
            won in a landslide.
            Combine that with a world-wide pandemic and you have the unthinkable
            scenario. But the unthinkable
            happened, when,
            on February 1, 2021, a military coup d'etat was carried out under
            the premise of vote irregularity.
            More than two
            years have passed since that brutal military coup occurred and
            it's time to shine a light on the beauty,
            biodiversity, and people of this country.
