            Why Kickstarter?
            <img src="img/p2.jpg" class="img-thumbnail rounded-0 border-0" alt="">

            Short Answer: Because of the partnership with Blurb I chose to work
            with Kickstarter in order to raise money to
            self-publish this book. Self-publishing is not for everyone, but it
            gives authors, writers, and photographers a
            chance to get their work out into the world.

            Longer Answer: Kickstarter is an all-or-nothing fundraising platform,
            meaning that if you don't reach the
            initial
            fundraising goal by the end of your campaign, then the project does not
            get funded. I am not sure I would
            meet the
            requirements to get enough pre-orders in order to print the three
            versions in bulk, so I will be doing the
            fulfillment myself once the book is finalized. It also gives me a chance
            to bring awareness in support of the
            movement for democracy.
            About me

            I am a writer, photographer, and regenerative farmer, with a strong
            interest in community building and
            re-invigorating the concepts of local and global villages. I spent
            most of my life in the San Francisco
            Bay Area and recently relocated to a small community in the MidCoast
            of Maine.

            I have lived and worked in Japan and Thailand and have traveled
            extensively throughout Southeast Asia.
            Hanging out in Burma: Laundry Lines and Democracy Hanging in the
            Balance is the culmination of three journeys
            to Burma which progressively deepened my appreciation of the beauty
            and warmth of its people and the plight
            of its democracy. My photographs have been exhibited at the Berkeley
            Art Center, the David Brower Center,
            and the Tides Foundation. I have a BA in English from Santa Clara
            University and an MA in Asian Studies
            from the University of San Francisco.

            Hanging Out in Burma: Laundry Lines and Democracy Hanging in the
            Balance, is a personal project that I
            have been working on for the past six years. Things just kept on
            changing in Burma after the 2015 election
            and as a result, the book project kept getting stalled. It's time
            to shine the light on Burma, on the people,
            the colors, the beauty, and a country with so much to save.
