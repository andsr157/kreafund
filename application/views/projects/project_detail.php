            Why Burma…Why now?

            It's the WINTER of 2023, and it certainly is NOT a good time to be hanging out in
            Burma. My name is
            Charlie Costello, and after six years, I am finally close to completing the book,
            Hanging Out in Burma: Laundry Lines and Democracy Hanging in the Balance.
            Just before my first visit in December 2011, the military began loosening its grip
            on the country. But when I
            arrived it still felt as if Big Brother was watching, that the authoritarian
            regime's
            oppressive rule still
            prevailed.

            This incredible country, the people and their cultural traditions and wisdom,
            the colors, the biodiversity,
            a country with so much to save - was calling out to me to Hang Out in Burma.Your
            support will help me complete the book and shine a light on BURMA amidst all of
            the things happening in
            the world.
            Balloons over Bagan, December 2011
            Balloons over Bagan, December 2011



            Burma is a country rich in mind, body, and soul, but sadly, a land that has been
            held hostage by its
            own military government. I want this project to tell my story, as I walk you
            through photos compiled over
            three trips, that will shine a light on the beauty, heart, and soul of the
            country and its people.

            Your support will pay homage to the strength and beauty of the people and
            their country and their quest for
            freedom that continues to this day.
            Charlie Costello in Bagan
            Charlie Costello in Bagan

